// creating the setup function
function setup() {

  // creating a canvas 550px wide and 400px high
  createCanvas(550, 400)

  // coloring the background 'red'
  background('red')
}

// creating the drawing function
function draw() {
  // tell p5 to print the the text string "Hello World!"
  // Positioning this text string at (x:30, y:30).
  text("Hello World!", 30, 30)
}
